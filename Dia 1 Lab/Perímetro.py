# Programa que exibe o perímetro de uma circunferência a partir de seu raio
import math
# Comando necessário para o funcionamento de alguns recursos matemáticos como o PI
raio = float(input("Entre com o raio em centímentros: "))
# Guarda o raio informado pelo usuário
perimetro = 2*math.pi*raio
# Formula perimetro = 2*pi*R
print(perimetro)